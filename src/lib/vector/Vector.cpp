#include "Vector.h"

Vector3D Math::Add(const Vector3D &a, const Vector3D &b) {
    return a + b;
}

Vector3D Math::Subtract(const Vector3D &a, const Vector3D &b) {
    return a - b;
}

Vector2D Math::Add(const Vector2D &a, const Vector2D &b) {
    return a + b;
}

Vector2D Math::Subtract(const Vector2D &a, const Vector2D &b) {
    return a - b;
}
